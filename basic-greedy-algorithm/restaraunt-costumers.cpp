#include <bits/stdc++.h>
 
using namespace std;
 
int32_t main() {
    ios_base::sync_with_stdio(false); cin.tie(0);
 
    int n; cin >> n;
    vector<pair<int, int>> cost;
 
    for (int i=1; i<=n; i++) {
        int a, b; cin >> a >> b;
        cost.push_back({a, b}); 
    }


 
 
    return 0;
}
